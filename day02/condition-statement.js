/* if...else statement */

if (studentExamScore >= 50) {
  console.log("You passed the exam");
} else if (studentExamScore < 50) {
  console.log("You failed the exam");
}  else if (studentExamScore === 100) {
  console.log("You got a perfect score");
} else {
  console.log("Invalid exam score");
}