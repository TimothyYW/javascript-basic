/*
    OPERATOR: 
    - +
    - -
    - /
    - *
    - %
*/

var number1 = 100;
var number2 = 5;

console.log(number1 + number2);
console.log(number1 - number2);
console.log(number1 / number2);
console.log(number1 * number2);

// Modulus/Module: Hasil sisa bagi
console.log(number1 % number2); //  100 % 5 -> 0
console.log(8 % 5); // Sisa bagi: 3
console.log(6 % 5); // Sisa bagi: 1

// Number bertemu dengan string
var angka1 = '10';
var angka2 = 5;
console.log(angka1 + angka2); // Join/digabungkan: 105
console.log(angka1 - angka2); // Matematika
console.log(angka1 / angka2); // Matematika
console.log(angka1 * angka2); // Matematika
